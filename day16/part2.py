layoutGrid = inputRaw.splitlines()

def traceBeam(rowIdx, colIdx, curDirection, alreadyTraveledGrid):
    while (rowIdx >= 0 and rowIdx < len(layoutGrid)) and \
    (colIdx >= 0 and colIdx < len(layoutGrid[0])):
        if curDirection in alreadyTraveledGrid[rowIdx][colIdx]:
            return
        alreadyTraveledGrid[rowIdx][colIdx].append(curDirection)

        curChar = layoutGrid[rowIdx][colIdx]
        if curChar == '.':
            if curDirection == 'N':
                rowIdx -= 1
            if curDirection == 'S':
                rowIdx += 1
            if curDirection == 'W':
                colIdx -= 1
            if curDirection == 'E':
                colIdx += 1
        if curChar == '-':
            if curDirection == 'W':
                colIdx -= 1
            elif curDirection == 'E':
                colIdx += 1
            else:
                traceBeam(rowIdx, colIdx-1, 'W', alreadyTraveledGrid)
                traceBeam(rowIdx, colIdx+1, 'E', alreadyTraveledGrid)
                return
        if curChar == '|':
            if curDirection == 'N':
                rowIdx -= 1
            elif curDirection == 'S':
                rowIdx += 1
            else:
                traceBeam(rowIdx-1, colIdx, 'N',alreadyTraveledGrid)
                traceBeam(rowIdx+1, colIdx, 'S', alreadyTraveledGrid)
                return
        if curChar == '/':
            if curDirection == 'N':
                curDirection = 'E'
                colIdx += 1
            elif curDirection == 'S':
                curDirection = 'W'
                colIdx -= 1
            elif curDirection == 'W':
                curDirection = 'S'
                rowIdx += 1
            elif curDirection == 'E':
                curDirection = 'N'
                rowIdx -= 1
        if curChar == '7':
            if curDirection == 'N':
                curDirection = 'W'
                colIdx -= 1
            elif curDirection == 'S':
                curDirection = 'E'
                colIdx += 1
            elif curDirection == 'W':
                curDirection = 'N'
                rowIdx -= 1
            elif curDirection == 'E':
                curDirection = 'S'
                rowIdx += 1
    return


def getTraceBeamCount(rowIdx, colIdx, curDirection):
    alreadyTraveledGrid = []
    for row in range(len(layoutGrid)):
        alreadyTraveledGrid.append([])
        for col in range(len(layoutGrid[0])):
            alreadyTraveledGrid[row].append([])

    traceBeam(rowIdx, colIdx, curDirection, alreadyTraveledGrid)

    tileCount = 0
    for row in alreadyTraveledGrid:
        for charList in row:
            if charList:
                tileCount += 1
    return tileCount

maxTileCount = 0
# top and bottom row entry
for colIdx in range(len(layoutGrid[0])):
    maxTileCount = max(maxTileCount, getTraceBeamCount(0, colIdx, 'S'))
    maxTileCount = max(maxTileCount, getTraceBeamCount(len(layoutGrid)-1, colIdx, 'N'))
# left and right col entry
for rowIdx in range(len(layoutGrid)):
    maxTileCount = max(maxTileCount, getTraceBeamCount(rowIdx, 0, 'E'))
    maxTileCount = max(maxTileCount, getTraceBeamCount(rowIdx, len(layoutGrid[0])-1, 'W'))

print(maxTileCount)
